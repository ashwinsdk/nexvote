@Injectable({ providedIn: 'root' })
export class I18nService {
    private currentLocale = signal<Locale>(this.loadLocale());
    private version = signal<number>(0); // Version counter to force updates

    locale = this.currentLocale.asReadonly();
    localeInfo = computed(() => SUPPORTED_LOCALES.find(l => l.code === this.currentLocale())!);
    locales = SUPPORTED_LOCALES;

    private loadLocale(): Locale {
        const stored = localStorage.getItem('nv_locale') as Locale | null;
        if (stored && TRANSLATIONS[stored]) return stored;
        return 'en';
    }

    setLocale(locale: Locale): void {
        this.currentLocale.set(locale);
        this.version.update(v => v + 1); // Increment version to trigger updates
        localStorage.setItem('nv_locale', locale);
        document.documentElement.lang = locale;

        // Force full page reload for complete translation update
        // This ensures all components re-render with new translations
        setTimeout(() => {
            window.location.reload();
        }, 100);
    }

    t(key: string): string {
        this.version(); // Read version signal to establish dependency
        const loc = this.currentLocale();
        return TRANSLATIONS[loc]?.[key] ?? TRANSLATIONS['en']?.[key] ?? key;
    }
}
